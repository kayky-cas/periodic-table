function messageToPeriodic(message) {
  if (message.length === 0) {
    return "";
  }

  for (const val of ptable) {
    if (message.startsWith(val.toLowerCase())) {
      const result = messageToPeriodic(message.substring(val.length));
      if (result !== null) {
        return val + result;
      }
    }
  }
  return null;
}

